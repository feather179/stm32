void OLEDIIC_Init() {
    GPIO_InitTypeDef GPIO_InitStructure;
    RCC_APB2PeriphClockCmd(	RCC_APB2Periph_GPIOB, ENABLE );	//使能GPIOB时钟

    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8 | GPIO_Pin_9;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP ;   //推挽输出
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(GPIOB, &GPIO_InitStructure);
    GPIO_SetBits(GPIOB, GPIO_Pin_8 | GPIO_Pin_9); 	//PB6,PB7 输出高



}

//产生IIC起始信号
void OLEDIIC_Start(void)
{
    OLEDSDA_OUT();     //sda线输出
    OLEDIIC_SDA = 1;
    OLEDIIC_SCL = 1;
    delay_us(4);
    OLEDIIC_SDA = 0; //START:when CLK is high,DATA change form high to low
    delay_us(4);
    OLEDIIC_SCL = 0; //钳住I2C总线，准备发送或接收数据
}
//产生IIC停止信号
void OLEDIIC_Stop(void)
{
    OLEDSDA_OUT();//sda线输出
    OLEDIIC_SCL = 0;
    OLEDIIC_SDA = 0; //STOP:when CLK is high DATA change form low to high
    delay_us(4);
    OLEDIIC_SCL = 1;
    OLEDIIC_SDA = 1; //发送I2C总线结束信号
    delay_us(4);
}
//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 OLEDIIC_Wait_Ack(void)
{
    u8 ucErrTime = 0;
    OLEDSDA_IN();      //SDA设置为输入
    OLEDIIC_SDA = 1;
    delay_us(1);
    OLEDIIC_SCL = 1;
    delay_us(1);
    while(OLEDREAD_SDA)
    {
        ucErrTime++;
        if(ucErrTime > 250)
        {
            OLEDIIC_Stop();
            return 1;
        }
    }
    OLEDIIC_SCL = 0; //时钟输出0
    return 0;
}
//产生ACK应答
void OLEDIIC_Ack(void)
{
    OLEDIIC_SCL = 0;
    OLEDSDA_OUT();
    OLEDIIC_SDA = 0;
    delay_us(2);
    OLEDIIC_SCL = 1;
    delay_us(2);
    OLEDIIC_SCL = 0;
}
//不产生ACK应答
void OLEDIIC_NAck(void)
{
    OLEDIIC_SCL = 0;
    OLEDSDA_OUT();
    OLEDIIC_SDA = 1;
    delay_us(2);
    OLEDIIC_SCL = 1;
    delay_us(2);
    OLEDIIC_SCL = 0;
}
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答
void OLEDIIC_Send_Byte(u8 txd)
{
    u8 t;
    OLEDSDA_OUT();
    OLEDIIC_SCL = 0; //拉低时钟开始数据传输
    for(t = 0; t < 8; t++)
    {
        OLEDIIC_SDA = (txd & 0x80) >> 7;
        txd <<= 1;
        delay_us(2);   //这三个延时都是必须的
        OLEDIIC_SCL = 1;
        delay_us(2);
        OLEDIIC_SCL = 0;
        delay_us(2);
    }
}
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK
u8 OLEDIIC_Read_Byte(unsigned char ack)
{
    unsigned char i, receive = 0;
    OLEDSDA_IN();//SDA设置为输入
    for(i = 0; i < 8; i++ )
    {
        OLEDIIC_SCL = 0;
        delay_us(2);
        OLEDIIC_SCL = 1;
        receive <<= 1;
        if(OLEDIIC_SDA)receive++;
        delay_us(1);
    }
    if (!ack)
        OLEDIIC_NAck();//发送nACK
    else
        OLEDIIC_Ack(); //发送ACK
    return receive;
}

void OLED_WR_Cmd(unsigned char cmd)
{
    OLEDIIC_Start();
    OLEDIIC_Send_Byte(0x78);
    OLEDIIC_Wait_Ack();
    OLEDIIC_Send_Byte(0x00);
    OLEDIIC_Wait_Ack();
    OLEDIIC_Send_Byte(cmd);
    OLEDIIC_Wait_Ack();
    OLEDIIC_Stop();
}

void OLED_WR_Data(unsigned char data)
{
    OLEDIIC_Start();
    OLEDIIC_Send_Byte(0x78);
    OLEDIIC_Wait_Ack();
    OLEDIIC_Send_Byte(0x40);
    OLEDIIC_Wait_Ack();
    OLEDIIC_Send_Byte(data);
    OLEDIIC_Wait_Ack();
    OLEDIIC_Stop();
}

void OLED_WR_Byte(u8 data, u8 cmd)
{
    if(cmd)
    {
        OLED_WR_Data(data);
    }
    else
    {
        OLED_WR_Cmd(data);
    }
}


//坐标设置
void OLED_Set_Pos(u8 x, u8 y)
{
    OLED_WR_Byte(0xb0 + y, OLED_CMD);
    OLED_WR_Byte((((x & 0xf0) >> 4) | 0x10), OLED_CMD);
    OLED_WR_Byte((x & 0x0f), OLED_CMD);
}


void OLED_Clear() {
    u8 i, n;
    for(i = 0; i < 8; i++)
    {
        OLED_WR_Byte(0xb0 + i, OLED_CMD);
        OLED_WR_Byte(0x00, OLED_CMD);
        OLED_WR_Byte(0x10, OLED_CMD);
        for(n = 0; n < 128; n++)
        {
            OLED_WR_Byte(0, OLED_DATA);
        }
    }
}


//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示
//size:选择字体 16/12

void OLED_ShowChar(u8 x, u8 y, u8 chr, u8 size)
{
    u8 c = 0, i = 0;
    c = chr - ' '; //得到偏移后的值
    if(x > Max_Column - 1)
    {
        x = 0;
        y = y + 2;
    }
    if(size == 16)
    {
        OLED_Set_Pos(x, y);
        for(i = 0; i < 8; i++)
        {
            OLED_WR_Byte(F8X16[c * 16 + i], OLED_DATA);
        }
        OLED_Set_Pos(x, y + 1);

        for(i = 0; i < 8; i++)
        {
            OLED_WR_Byte(F8X16[c * 16 + i + 8], OLED_DATA);
        }
    }
    else
    {
        OLED_Set_Pos(x, y);
        for(i = 0; i < 6; i++)
        {
            OLED_WR_Byte(F6X8[c][i], OLED_DATA);
        }
    }

}

void OLED_WR8Char(u8 x, u8 y, u8 chr)
{
    OLED_ShowChar(x * 6, y, chr, 12);
}

void OLED_WR16Char(u8 x, u8 y, u8 chr)
{
    OLED_ShowChar(x * 8, y, chr, 16);
}

void OLED_Init() {
    OLEDIIC_Init();
    delay_ms(5);

    OLED_WR_Byte(0xAE, OLED_CMD); //--display off
    OLED_WR_Byte(0x00, OLED_CMD); //---set low column address
    OLED_WR_Byte(0x10, OLED_CMD); //---set high column address
    OLED_WR_Byte(0x40, OLED_CMD); //--set start line address
    OLED_WR_Byte(0xB0, OLED_CMD); //--set page address
    OLED_WR_Byte(0x81, OLED_CMD); // contract control
    OLED_WR_Byte(0xFF, OLED_CMD); //--128
    OLED_WR_Byte(0xA1, OLED_CMD); //set segment remap
    OLED_WR_Byte(0xA6, OLED_CMD); //--normal / reverse
    OLED_WR_Byte(0xA8, OLED_CMD); //--set multiplex ratio(1 to 64)
    OLED_WR_Byte(0x3F, OLED_CMD); //--1/32 duty
    OLED_WR_Byte(0xC8, OLED_CMD); //Com scan direction
    OLED_WR_Byte(0xD3, OLED_CMD); //-set display offset
    OLED_WR_Byte(0x00, OLED_CMD); //

    OLED_WR_Byte(0xD5, OLED_CMD); //set osc division
    OLED_WR_Byte(0x80, OLED_CMD); //

    OLED_WR_Byte(0xD8, OLED_CMD); //set area color mode off
    OLED_WR_Byte(0x05, OLED_CMD); //

    OLED_WR_Byte(0xD9, OLED_CMD); //Set Pre-Charge Period
    OLED_WR_Byte(0xF1, OLED_CMD); //

    OLED_WR_Byte(0xDA, OLED_CMD); //set com pin configuartion
    OLED_WR_Byte(0x12, OLED_CMD); //

    OLED_WR_Byte(0xDB, OLED_CMD); //set Vcomh
    OLED_WR_Byte(0x30, OLED_CMD); //

    OLED_WR_Byte(0x8D, OLED_CMD); //set charge pump enable
    OLED_WR_Byte(0x14, OLED_CMD); //

    OLED_WR_Byte(0xAF, OLED_CMD); //--turn on oled panel
    delay_ms(5);

    OLED_Clear();
}





